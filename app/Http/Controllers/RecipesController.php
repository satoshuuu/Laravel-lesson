     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        //
        $recipe = new Recipe;
        $recipe->recipe_name = $request->recipe_name;
        $recipe->content = $request->content;
        $recipe->img = $request->img;
        $recipe->save;

        return redirect('/');
    }

